    public boolean validateInformation() {
        if (jTextFieldName.getText().equals("")
                || jTextFieldLastName.getText().equals("")
                || jTextFieldCardID.getText().equals("")
                || jTextFieldEmail.getText().equals("")
                || (jRBF.isSelected() == false && jRBM.isSelected() == false)
                || jTextFieldUserName.getText().equals("")
                || jPassword.getPassword().equals("")
                || jConfirmPassword.getPassword().equals("")
                || checkboxTyC.getState() == false) {
            JOptionPane.showMessageDialog(null, "DEBE LLENAR TODOS LOS CAMPOS Y ACEPTAR LOS TÉRMINOS Y CONDICIONES", "CAMPOS INCOMPLETOS", JOptionPane.ERROR_MESSAGE);
            return false;
        } else if (!String.copyValueOf(jPassword.getPassword()).equals(String.copyValueOf(jConfirmPassword.getPassword()))) {
            JOptionPane.showMessageDialog(null, "LAS CONTRASEÑAS NO COINCIDEN, VERIFÍQUELAS", "LAS CONTRASEÑAS NO COINCIDEN", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

