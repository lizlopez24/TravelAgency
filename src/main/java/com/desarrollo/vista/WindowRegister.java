    private void jBtnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegisterActionPerformed
        String name = jTextFieldName.getText();
        String lastName = jTextFieldLastName.getText();
        String cardID = jTextFieldCardID.getText();
        String email = jTextFieldEmail.getText();
        String genero;
        if (jRBF.isSelected()) {
            genero = "Femenino";
        } else {
            genero = "Masculino";
        }
        String user = jTextFieldUserName.getText();
        String password = String.copyValueOf(jPassword.getPassword());

        if (validateInformation()) {
            int register = controlador.insertEmployee(name, lastName, cardID, email, genero, user, password);
            if (register != 0) {
                JOptionPane.showMessageDialog(null, "SE HA REGISTRADO CORRECTAMENTE. YA PUEDE INICIAR SESIÓN", "REGISTRO EXITOSO", JOptionPane.INFORMATION_MESSAGE);
                WelcomeLogin windows= new WelcomeLogin();
                windows.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "ERROR AL REGISTRAR");
            }
        }
    }//GEN-LAST:event_jBtnRegisterActionPerformed

    private void jCheckBoxVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxVerActionPerformed
        if (jCheckBoxVer.isSelected()) {
            jPassword.setEchoChar((char) 0);
            jConfirmPassword.setEchoChar((char) 0);
        } else {
            jPassword.setEchoChar('*');
            jConfirmPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_jCheckBoxVerActionPerformed

